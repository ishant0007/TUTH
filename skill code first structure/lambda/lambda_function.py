class LaunchRequestHandler(AbstractRequestHandler):
    """Handler for Skill Launch."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool

        return ask_utils.is_request_type("LaunchRequest")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        speak_output = """Hi there, My name is TUTH or as they say, The unpaid therapist. and I'm here to support you as we work together towards your goals. 
        I want to create a safe and supportive space where you can share your thoughts and feelings without judgment. Can you tell me a bit about what brings you here today?"""

        return (
            handler_input.response_builder
                .speak(speak_output)
                .ask(speak_output)
                .response
        )


